class ColabBackend(AbstractBackend):
    """Google Colab compute backend.

    Generates a self-contained Jupyter notebook from a kdream recipe that can
    be opened directly in Google Colab. The notebook handles:

    * GPU runtime detection
    * Repository cloning and dependency installation (via ``uv``)
    * Model downloading (HuggingFace Hub, direct URL, CivitAI)
    * Parameterised inference with pre-filled input values
    * Output display (images via IPython.display, audio, video)

    If ``gdrive_credentials`` (or the ``KDREAM_GDRIVE_CREDENTIALS`` env var) is
    set to the path of a Google service-account JSON key, the notebook is
    uploaded to Drive and a direct Colab URL is printed automatically.

    Usage::

        kdream install stable-diffusion-xl-base --backend colab
        kdream run stable-diffusion-xl-base --backend colab --prompt "red panda"
    """

    name = "colab"

    def __init__(
        self,
        gdrive_credentials: str | None = None,
        verbose: bool = False,
    ):
        self.gdrive_credentials = gdrive_credentials or os.environ.get(
            "KDREAM_GDRIVE_CREDENTIALS"
        )
        self.verbose = verbose
        self._builder = NotebookBuilder()

    # ------------------------------------------------------------------
    # AbstractBackend interface
    # ------------------------------------------------------------------

    def install(
        self,
        recipe: Recipe,
        cache_dir: Path,
        force_reinstall: bool = False,
    ):
        """Generate the base Colab notebook and save it to the cache directory.

        No network calls are made during install — model downloads happen inside
        the notebook when the user opens it in Colab.

        Returns:
            :class:`~kdream.core.runner.PackageInfo` with ``path`` pointing to
            the directory containing the generated notebook.
        """
        from kdream.core.runner import PackageInfo

        pkg_dir = cache_dir / recipe.metadata.name
        nb_dir = pkg_dir / "colab"
        nb_dir.mkdir(parents=True, exist_ok=True)
        nb_path = nb_dir / f"kdream-{recipe.metadata.name}.ipynb"

        if nb_path.exists() and not force_reinstall:
            console.print(
                f"  [dim]Notebook already generated: {nb_path}[/dim]\n"
                "  Use --force-reinstall to regenerate."
            )
        else:
            console.print(
                f"\n[bold][1/1][/bold] Generating Colab notebook for "
                f"[cyan]{recipe.metadata.name}[/cyan]…"
            )
            notebook = self._builder.build(recipe)
            nb_path.write_text(json.dumps(notebook, indent=2), encoding="utf-8")
            console.print(f"  [green]✓ Notebook saved:[/green] {nb_path}")

        return PackageInfo(
            recipe_name=recipe.metadata.name,
            path=nb_dir,
            ready=nb_path.exists(),
            venv_path=nb_dir,
            repo_path=nb_dir,
            models_path=nb_dir,
        )

    def run(self, package, inputs: dict[str, Any]) -> dict[str, Any]:
        """Inject *inputs* into the notebook and provide a Colab URL.

        Generates a run-specific notebook with input values pre-filled, then:
        * Uploads to Google Drive (if credentials configured) and returns a URL.
        * Falls back to printing manual upload instructions.

        Returns:
            Dict with keys ``notebook_path``, ``colab_url``, and ``instructions``.
        """
        nb_base = package.path / f"kdream-{package.recipe_name}.ipynb"
        if not nb_base.exists():
            raise BackendError(
                f"Colab notebook not found at {nb_base}. "
                "Run 'kdream install --backend colab <recipe>' first."
            )

        # Load recipe for input injection
        recipe_file = nb_base.parent.parent / "kdream-recipe.yaml"
        recipe: Recipe | None = None
        if recipe_file.exists():
            from kdream.core.recipe import load_recipe
            recipe = load_recipe(str(recipe_file))
        else:
            try:
                from kdream.core.registry import RegistryClient
                recipe = RegistryClient().fetch_recipe(package.recipe_name)
            except Exception:
                pass

        # Generate run-specific notebook with inputs injected
        nb_run_path = nb_base.parent / f"kdream-{package.recipe_name}-run.ipynb"
        if recipe is not None:
            notebook = self._builder.build(recipe, inputs)
            nb_run_path.write_text(json.dumps(notebook, indent=2), encoding="utf-8")
        else:
            import shutil
            shutil.copy2(str(nb_base), str(nb_run_path))

        colab_url = self._resolve_colab_url(recipe, nb_run_path)

        # User-facing instructions
        if colab_url:
            url_section = (
                f"[bold green]Open in Colab:[/bold green]\n[link]{colab_url}[/link]"
            )
        else:
            url_section = (
                "To run in Colab:\n"
                "1. Go to [link]https://colab.research.google.com[/link]\n"
                "2. File \u2192 Upload notebook \u2192 select the file above\n"
                "3. Runtime \u2192 Run all  (Ctrl+F9)\n\n"
                "[dim]Tip: set KDREAM_GDRIVE_CREDENTIALS to a service-account JSON\n"
                "for automatic Drive upload and direct Colab URLs.[/dim]"
            )

        console.print(Panel(
            f"[bold]Colab Notebook Ready[/bold]\n\n"
            f"Notebook: [cyan]{nb_run_path}[/cyan]\n\n"
            + url_section,
            title="kdream Colab",
            expand=False,
        ))

        return {
            "notebook_path": str(nb_run_path),
            "colab_url": colab_url or "",
            "instructions": (
                f"Open in Colab: {colab_url}"
                if colab_url else
                f"Upload {nb_run_path} to Google Colab to run inference."
            ),
        }

    def is_installed(self, recipe_name: str, cache_dir: Path) -> bool:
        """Return True if the Colab notebook has already been generated."""
        nb_path = cache_dir / recipe_name / "colab" / f"kdream-{recipe_name}.ipynb"
        return nb_path.exists()

    def generate_notebook(
        self,
        recipe: Recipe,
        inputs: dict[str, Any] | None = None,
        output_path: str | None = None,
    ) -> Path:
        """Generate a Colab notebook for *recipe* and save it.

        Convenience method for use outside the install/run flow
        (e.g. from the CLI or scripts).

        Args:
            recipe:      Recipe to generate the notebook from.
            inputs:      Optional input values to pre-fill.
            output_path: Where to save the file (defaults to cwd).

        Returns:
            Path to the saved ``.ipynb`` file.
        """
        notebook = self._builder.build(recipe, inputs or {})
        dest = Path(output_path) if output_path else (
            Path.cwd() / f"kdream-{recipe.metadata.name}.ipynb"
        )
        dest.parent.mkdir(parents=True, exist_ok=True)
        dest.write_text(json.dumps(notebook, indent=2), encoding="utf-8")
        return dest

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _resolve_colab_url(self, recipe: Recipe | None, nb_path: Path) -> str | None:
        """Try to obtain a usable Colab URL for the notebook.

        Priority:
        1. Upload to Google Drive (if credentials configured).
        2. Build a GitHub Colab URL (if recipe source is on GitHub).
        3. Return ``None`` — manual upload required.
        """
        if self.gdrive_credentials:
            try:
                url = GoogleDriveUploader(self.gdrive_credentials).upload(nb_path, nb_path.name)
                console.print("  [green]✓ Uploaded to Google Drive[/green]")
                return url
            except BackendError as exc:
                console.print(f"  [yellow]⚠ Drive upload failed:[/yellow] {exc}")
            except Exception as exc:
                if self.verbose:
                    console.print(f"  [dim]Drive upload error: {exc}[/dim]")

        if recipe is not None:
            return _colab_github_url(recipe)

        return None
