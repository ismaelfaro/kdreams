"""Google Colab backend — generates self-contained Colab notebooks from recipes."""
from __future__ import annotations

import json
import os
import textwrap
import uuid
from pathlib import Path
from typing import Any

from rich.console import Console
from rich.panel import Panel

from kdream.backends.base import AbstractBackend
from kdream.core.recipe import Recipe
from kdream.exceptions import BackendError

console = Console()


# ---------------------------------------------------------------------------
# Notebook builder
# ---------------------------------------------------------------------------

class NotebookBuilder:
    """Builds a self-contained Jupyter/Colab notebook from a kdream recipe."""

    _GPU_CHECK = textwrap.dedent("""\
        import subprocess

        result = subprocess.run(["nvidia-smi"], capture_output=True)
        if result.returncode == 0:
            print("\\u2713 GPU runtime detected")
            print(result.stdout.decode()[:500])
        else:
            print("\\u26a0 No GPU detected \\u2014 running on CPU.")
            print("Tip: Runtime \\u2192 Change runtime type \\u2192 T4 GPU for best performance.")
    """)

    def build(self, recipe: Recipe, inputs: dict[str, Any] | None = None) -> dict:
        """Return a complete notebook dict (ready to be serialised as .ipynb)."""
        cells: list[dict] = [
            self._markdown_cell(
                f"# {recipe.metadata.name}\n\n"
                f"{recipe.metadata.description}\n\n"
                f"**Tags:** {', '.join(recipe.metadata.tags)}  \n"
                f"**License:** {recipe.metadata.license}  \n"
                f"**Generated by:** [kdream](https://github.com/ismaelfaro/kdreams)\n"
            ),
            self._code_cell(self._GPU_CHECK, cell_id="hardware_check"),
            self._code_cell(self._clone_code(recipe), cell_id="clone_repo"),
            self._code_cell(self._deps_code(recipe), cell_id="install_deps"),
        ]

        if recipe.models:
            cells.append(self._code_cell(self._models_code(recipe), cell_id="download_models"))

        cells.append(
            self._code_cell(self._inputs_code(recipe, inputs or {}), cell_id="set_inputs")
        )
        cells.append(self._code_cell(self._run_code(recipe), cell_id="run_inference"))
        cells.append(self._code_cell(self._outputs_code(recipe), cell_id="show_outputs"))

        return {
            "nbformat": 4,
            "nbformat_minor": 0,
            "metadata": {
                "colab": {
                    "name": f"kdream-{recipe.metadata.name}.ipynb",
                    "provenance": [],
                    "gpuType": "T4",
                },
                "accelerator": "GPU",
                "kernelspec": {"name": "python3", "display_name": "Python 3"},
                "language_info": {"name": "python"},
            },
            "cells": cells,
        }

    # ------------------------------------------------------------------
    # Cell helpers
    # ------------------------------------------------------------------

    def _markdown_cell(self, source: str) -> dict:
        return {
            "cell_type": "markdown",
            "metadata": {"id": _uid()},
            "source": source,
        }

    def _code_cell(self, source: str, cell_id: str | None = None) -> dict:
        return {
            "cell_type": "code",
            "metadata": {"id": cell_id or _uid()},
            "source": source,
            "execution_count": None,
            "outputs": [],
        }

    # ------------------------------------------------------------------
    # Source generators
    # ------------------------------------------------------------------

    def _clone_code(self, recipe: Recipe) -> str:
        repo = recipe.source.repo
        ref = recipe.source.ref or "main"
        name = recipe.metadata.name
        return textwrap.dedent(f"""\
            import os, subprocess

            REPO_URL = {repo!r}
            REF = {ref!r}
            WORK_DIR = f"/content/{name}"

            if not os.path.exists(WORK_DIR):
                subprocess.run(
                    ["git", "clone", "--depth", "1", "--branch", REF, REPO_URL, WORK_DIR],
                    check=True,
                )
            else:
                print(f"Repository already cloned at {{WORK_DIR}}")

            os.chdir(WORK_DIR)
            print(f"Working directory: {{os.getcwd()}}")
        """)

    def _deps_code(self, recipe: Recipe) -> str:
        extras = recipe.source.install_extras
        extras_joined = ",".join(extras)
        lines: list[str] = [
            "import subprocess, sys, os",
            "",
            "subprocess.run([sys.executable, '-m', 'pip', 'install', '-q', 'uv'], check=True)",
            "",
        ]
        if recipe.source.skip_package_install:
            lines.append("print('Package install skipped (skip_package_install=true)')")
        else:
            lines += [
                "for req_file in ['requirements.txt', 'requirements/base.txt',"
                " 'requirements/main.txt']:",
                "    if os.path.exists(req_file):",
                "        subprocess.run(",
                "            [sys.executable, '-m', 'uv', 'pip', 'install', '-q',"
                " '-r', req_file],",
                "            check=True,",
                "        )",
                "        break",
                "",
                "if os.path.exists('pyproject.toml') or os.path.exists('setup.py'):",
                f"    extra = '[{extras_joined}]' if {extras_joined!r} else ''",
                "    subprocess.run(",
                "        [sys.executable, '-m', 'uv', 'pip', 'install', '-q',"
                " '-e', f'.{extra}'],",
                "        check=True,",
                "    )",
                "",
                "print('\\u2713 Dependencies installed')",
            ]
        return "\n".join(lines)

    def _models_code(self, recipe: Recipe) -> str:
        lines: list[str] = [
            "from pathlib import Path",
            "",
            "MODELS_DIR = Path('/content/models')",
            "MODELS_DIR.mkdir(parents=True, exist_ok=True)",
            "",
        ]
        for model in recipe.models:
            dest_expr = f"MODELS_DIR / {model.destination!r}"
            if model.source == "huggingface":
                lines += [
                    f"# Download model: {model.name}",
                    "try:",
                    "    from huggingface_hub import snapshot_download",
                    f"    snapshot_download(",
                    f"        repo_id={model.id!r},",
                    f"        local_dir={dest_expr},",
                    "        ignore_patterns=['*.msgpack', '*.h5', 'flax_model*'],",
                    "    )",
                    f"    print('\\u2713 Downloaded {model.id}')",
                    "except Exception as _e:",
                    f"    print(f'\\u26a0 Failed to download {model.id}: {{_e}}')",
                    "",
                ]
            elif model.source == "url":
                lines += [
                    f"# Download model: {model.name}",
                    "import urllib.request",
                    f"_dest = {dest_expr} / Path({model.id!r}).name",
                    "_dest.parent.mkdir(parents=True, exist_ok=True)",
                    "if not _dest.exists():",
                    f"    print('Downloading {model.name}...')",
                    f"    urllib.request.urlretrieve({model.id!r}, _dest)",
                    f"    print('\\u2713 Downloaded {model.name}')",
                    "else:",
                    f"    print('\\u2713 {model.name} already present')",
                    "",
                ]
            elif model.source == "civitai":
                lines += [
                    f"# CivitAI model: {model.name}",
                    "import urllib.request, os",
                    "_token = os.environ.get('CIVITAI_TOKEN', '')",
                    "_headers = {'Authorization': f'Bearer {{_token}}'} if _token else {}",
                    f"_url = f'https://civitai.com/api/download/models/{model.id}'",
                    f"_dest = {dest_expr} / '{model.name}.safetensors'",
                    "_dest.parent.mkdir(parents=True, exist_ok=True)",
                    "if not _dest.exists():",
                    "    _req = urllib.request.Request(_url, headers=_headers)",
                    "    with urllib.request.urlopen(_req) as _r, open(_dest, 'wb') as _f:",
                    "        _f.write(_r.read())",
                    f"    print('\\u2713 Downloaded {model.name}')",
                    "",
                ]
        lines.append("print('\\u2713 All models ready')")
        return "\n".join(lines)

    def _inputs_code(self, recipe: Recipe, inputs: dict[str, Any]) -> str:
        lines: list[str] = [
            "# ── Inference inputs ─────────────────────────────────────────",
            "# Edit these values before running the inference cell.",
            "",
        ]
        for name, spec in recipe.inputs.items():
            raw = inputs.get(name)
            if raw is None:
                raw = spec.default
            comment = f"  # {spec.description}" if spec.description else ""
            if spec.type == "string":
                val_repr = repr(str(raw)) if raw is not None else '""'
            elif spec.type in ("integer", "float"):
                val_repr = str(raw) if raw is not None else "0"
            elif spec.type == "boolean":
                val_repr = "True" if raw else "False"
            else:
                val_repr = repr(raw) if raw is not None else "None"
            lines.append(f"{name} = {val_repr}{comment}")
        return "\n".join(lines)

    def _run_code(self, recipe: Recipe) -> str:
        script = recipe.entrypoint.script
        entry_type = recipe.entrypoint.type
        name = recipe.metadata.name
        arg_mapping = recipe.entrypoint.args_mapping or {}

        lines: list[str] = [
            "import subprocess, sys, os",
            "",
            f"WORK_DIR = '/content/{name}'",
            "os.chdir(WORK_DIR)",
            "",
        ]

        if entry_type == "gradio":
            lines += [
                "# Gradio app — launch and wait for the public URL",
                "import time",
                f"_proc = subprocess.Popen([sys.executable, {script!r}],",
                "                         stdout=subprocess.PIPE, stderr=subprocess.STDOUT)",
                "time.sleep(8)",
                "for _line in iter(_proc.stdout.readline, b''):",
                "    _dec = _line.decode()",
                "    if 'gradio.live' in _dec or 'running on' in _dec.lower():",
                "        print('Gradio URL:', _dec.strip())",
                "        break",
            ]
        else:
            lines.append(f"_cmd = [sys.executable, f'{{WORK_DIR}}/{script}']")
            for inp_name, spec in recipe.inputs.items():
                flag = arg_mapping.get(inp_name, inp_name.replace("_", "-"))
                if spec.type == "boolean":
                    lines += [
                        f"if {inp_name}:",
                        f"    _cmd += ['--{flag}']",
                    ]
                else:
                    lines.append(f"_cmd += ['--{flag}', str({inp_name})]")
            lines += [
                "",
                "print('Running:', ' '.join(_cmd[:5]), '...')",
                "_result = subprocess.run(_cmd, capture_output=True, text=True, cwd=WORK_DIR)",
                "print(_result.stdout[-3000:] if _result.stdout else '')",
                "if _result.returncode != 0:",
                "    print('STDERR:', _result.stderr[-2000:])",
                "    raise RuntimeError(f'Inference failed (exit {_result.returncode})')",
                "print('\\u2713 Inference complete')",
            ]
        return "\n".join(lines)

    def _outputs_code(self, recipe: Recipe) -> str:
        if not recipe.outputs:
            return "print('No outputs defined in recipe.')"

        lines: list[str] = ["import glob"]
        _ext_media = {
            ".png": "image", ".jpg": "image", ".jpeg": "image",
            ".gif": "image", ".webp": "image",
            ".mp4": "video", ".webm": "video",
            ".wav": "audio", ".mp3": "audio", ".flac": "audio",
        }

        for out in recipe.outputs:
            path_pat = out.path or ""
            ext = Path(path_pat).suffix.lower() if path_pat else ""
            media = _ext_media.get(ext, "file")

            lines += [
                f"# Output: {out.name}",
                f"_files = sorted(glob.glob('outputs/*{ext}') +"
                f" glob.glob('/content/{recipe.metadata.name}/outputs/*{ext}'))",
                "if _files:",
                "    _latest = _files[-1]",
                "    print(f'Output: {{_latest}}')",
            ]
            if media == "image":
                lines += [
                    "    from IPython.display import Image, display",
                    "    display(Image(filename=_latest))",
                ]
            elif media == "audio":
                lines += [
                    "    from IPython.display import Audio, display",
                    "    display(Audio(filename=_latest))",
                ]
            elif media == "video":
                lines += [
                    "    from IPython.display import HTML, display",
                    "    display(HTML(f'<video controls width=640>"
                    "<source src=\"{_latest}\"></video>'))",
                ]
            lines += ["else:", f"    print('No output found for {out.name}')", ""]

        return "\n".join(lines)


