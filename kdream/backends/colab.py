class GoogleDriveUploader:
    """Upload notebooks to Google Drive and return a Colab URL."""

    SCOPES = ["https://www.googleapis.com/auth/drive.file"]

    def __init__(self, credentials_path: str):
        self.credentials_path = credentials_path

    def upload(self, notebook_path: Path, name: str) -> str:
        """Upload *notebook_path* to Drive and return the Colab open URL."""
        try:
            from google.oauth2 import service_account  # type: ignore[import]
            from googleapiclient.discovery import build  # type: ignore[import]
            from googleapiclient.http import MediaFileUpload  # type: ignore[import]
        except ImportError:
            raise BackendError(
                "Google Drive upload requires 'google-auth' and 'google-api-python-client'.\n"
                "Install them with: pip install 'kdream[colab]'"
            )

        creds = service_account.Credentials.from_service_account_file(
            self.credentials_path, scopes=self.SCOPES
        )
        service = build("drive", "v3", credentials=creds)

        file_meta = {
            "name": name,
            "mimeType": "application/vnd.google.colaboratory",
        }
        media = MediaFileUpload(str(notebook_path), mimetype="application/json")
        uploaded = (
            service.files()
            .create(body=file_meta, media_body=media, fields="id")
            .execute()
        )
        file_id = uploaded["id"]
        # Make publicly viewable so anyone with the link can open it
        service.permissions().create(
            fileId=file_id,
            body={"type": "anyone", "role": "reader"},
        ).execute()

        return f"https://colab.research.google.com/drive/{file_id}"


# ---------------------------------------------------------------------------
# Utilities
# ---------------------------------------------------------------------------

def _uid() -> str:
    return uuid.uuid4().hex[:12]


def _colab_github_url(recipe: Recipe) -> str | None:
    """Build a colab.research.google.com/github/… URL when the recipe's repo
    is on GitHub and the notebook is committed there."""
    repo = recipe.source.repo or ""
    if "github.com" not in repo:
        return None
    gh_path = repo.removeprefix("https://").removeprefix("github.com/").strip("/")
    ref = recipe.source.ref or "main"
    nb_path = f"kdream/recipes/notebooks/{recipe.metadata.name}.ipynb"
    return f"https://colab.research.google.com/github/{gh_path}/blob/{ref}/{nb_path}"


