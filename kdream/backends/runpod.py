class RunPodBackend(AbstractBackend):
    """RunPod.io serverless compute backend.

    Generates a Dockerfile and handler.py for serverless endpoint deployment.
    The user builds a Docker image, pushes it, registers a RunPod serverless
    endpoint, then kdream submits jobs via the RunPod API.

    Workflow::

        # 1. Generate artifacts
        kdream runpod generate stable-diffusion-xl-base

        # 2. Build & push image
        cd ~/.kdream/cache/stable-diffusion-xl-base/runpod
        docker build -t myuser/stable-diffusion-xl-base:latest .
        docker push myuser/stable-diffusion-xl-base:latest

        # 3. Register endpoint at runpod.io, then run:
        kdream run stable-diffusion-xl-base --backend runpod \\
            --endpoint-id ep-xxxx --prompt "red panda"

    """

    name = "runpod"

    def __init__(
        self,
        api_key: str | None = None,
        endpoint_id: str | None = None,
        gpu_type: str = "NVIDIA GeForce RTX 3090",
        docker_image: str | None = None,
        terminate_after_run: bool = True,
        verbose: bool = False,
    ):
        self.api_key = api_key or os.environ.get("RUNPOD_API_KEY")
        self.endpoint_id = endpoint_id or os.environ.get("RUNPOD_ENDPOINT_ID")
        self.gpu_type = gpu_type
        self.docker_image = docker_image
        self.terminate_after_run = terminate_after_run
        self.verbose = verbose
        self._dockerfile_builder = DockerfileBuilder()
        self._handler_builder = HandlerBuilder()

    # ------------------------------------------------------------------
    # AbstractBackend interface
    # ------------------------------------------------------------------

    def install(
        self,
        recipe: Recipe,
        cache_dir: Path,
        force_reinstall: bool = False,
    ):
        """Generate Dockerfile and handler.py for a RunPod serverless endpoint.

        No network calls are made — artifacts are written to
        ``cache_dir/<name>/runpod/``.

        Returns:
            :class:`~kdream.core.runner.PackageInfo` with ``path`` pointing to
            the runpod artifacts directory.
        """
        from kdream.core.runner import PackageInfo

        pkg_dir = cache_dir / recipe.metadata.name
        runpod_dir = pkg_dir / "runpod"
        runpod_dir.mkdir(parents=True, exist_ok=True)

        dockerfile_path = runpod_dir / "Dockerfile"
        handler_path = runpod_dir / "handler.py"
        config_path = runpod_dir / "config.json"

        if dockerfile_path.exists() and not force_reinstall:
            console.print(
                f"  [dim]RunPod artifacts already generated: {runpod_dir}[/dim]\n"
                "  Use --force-reinstall to regenerate."
            )
        else:
            console.print(
                f"\n[bold][1/3][/bold] Generating Dockerfile for "
                f"[cyan]{recipe.metadata.name}[/cyan]…"
            )
            dockerfile_content = self._dockerfile_builder.build(recipe)
            dockerfile_path.write_text(dockerfile_content, encoding="utf-8")
            console.print(f"  [green]✓ Dockerfile saved:[/green] {dockerfile_path}")

            console.print(
                f"\n[bold][2/3][/bold] Generating handler.py for "
                f"[cyan]{recipe.metadata.name}[/cyan]…"
            )
            handler_content = self._handler_builder.build(recipe)
            handler_path.write_text(handler_content, encoding="utf-8")
            console.print(f"  [green]✓ handler.py saved:[/green] {handler_path}")

            console.print("\n[bold][3/3][/bold] Saving config.json…")
            config = {
                "recipe_name": recipe.metadata.name,
                "gpu_type": self.gpu_type,
                "docker_image": self.docker_image or f"<dockerhub-user>/{recipe.metadata.name}:latest",
                "endpoint_id": self.endpoint_id or "",
            }
            config_path.write_text(json.dumps(config, indent=2), encoding="utf-8")
            console.print(f"  [green]✓ config.json saved:[/green] {config_path}")

            self._print_instructions(recipe, runpod_dir)

        return PackageInfo(
            recipe_name=recipe.metadata.name,
            path=runpod_dir,
            ready=True,
            venv_path=runpod_dir,
            repo_path=runpod_dir,
            models_path=runpod_dir,
        )

    def run(self, package, inputs: dict[str, Any]) -> dict[str, Any]:
        """Submit a job to a RunPod serverless endpoint.

        Requires the endpoint to be registered and ``endpoint_id`` configured
        (either via constructor or ``RUNPOD_ENDPOINT_ID`` environment variable).

        Returns:
            Dict with keys ``output``, ``outputs``, and ``job_id``.
        """
        # Load config.json for endpoint_id if not already set
        config_path = package.path / "config.json"
        endpoint_id = self.endpoint_id
        if not endpoint_id and config_path.exists():
            config = json.loads(config_path.read_text(encoding="utf-8"))
            endpoint_id = config.get("endpoint_id") or None

        if not endpoint_id:
            raise BackendError(
                "No RunPod endpoint ID configured.\n\n"
                "To set up a serverless endpoint:\n"
                f"  1. cd {package.path}\n"
                f"  2. docker build -t <image> . && docker push <image>\n"
                "  3. Register endpoint at runpod.io → Serverless → New Endpoint\n"
                "  4. Re-run with: --endpoint-id <id>  or  RUNPOD_ENDPOINT_ID=<id>"
            )

        if not self.api_key:
            raise BackendError(
                "No RunPod API key configured. "
                "Set RUNPOD_API_KEY environment variable or pass api_key to RunPodBackend."
            )

        try:
            import runpod  # type: ignore[import]
        except ImportError:
            raise BackendError(
                "The 'runpod' package is required for the RunPod backend.\n"
                "Install it with: pip install 'kdream[runpod]'"
            )

        runpod.api_key = self.api_key
        endpoint = runpod.Endpoint(endpoint_id)

        try:
            result = endpoint.run_sync(inputs, timeout=300)
        except TimeoutError:
            raise BackendError(
                "RunPod job timed out after 300 seconds. "
                "Consider using async mode for long-running jobs."
            )
        except Exception as exc:
            raise BackendError(f"RunPod endpoint error: {exc}") from exc

        if isinstance(result, dict) and "error" in result:
            raise BackendError(f"RunPod job failed: {result['error']}")

        return result if isinstance(result, dict) else {"output": result, "outputs": {}}

    def is_installed(self, recipe_name: str, cache_dir: Path) -> bool:
        """Return True if Dockerfile has been generated for *recipe_name*."""
        return (cache_dir / recipe_name / "runpod" / "Dockerfile").exists()

    def generate_artifacts(
        self,
        recipe: Recipe,
        output_dir: Path | str | None = None,
        inputs: dict[str, Any] | None = None,
    ) -> Path:
        """Generate Dockerfile and handler.py to *output_dir*.

        Convenience method for use outside the install/run flow.

        Args:
            recipe:     Recipe to generate artifacts from.
            output_dir: Directory to write files (default: cwd).
            inputs:     Ignored (present for API symmetry with other backends).

        Returns:
            Path to the directory containing the generated artifacts.
        """
        dest = Path(output_dir) if output_dir else Path.cwd()
        dest.mkdir(parents=True, exist_ok=True)

        dockerfile_content = self._dockerfile_builder.build(recipe)
        (dest / "Dockerfile").write_text(dockerfile_content, encoding="utf-8")

        handler_content = self._handler_builder.build(recipe)
        (dest / "handler.py").write_text(handler_content, encoding="utf-8")

        config = {
            "recipe_name": recipe.metadata.name,
            "gpu_type": self.gpu_type,
            "docker_image": self.docker_image or f"<dockerhub-user>/{recipe.metadata.name}:latest",
            "endpoint_id": self.endpoint_id or "",
        }
        (dest / "config.json").write_text(json.dumps(config, indent=2), encoding="utf-8")

        return dest

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def _print_instructions(self, recipe: Recipe, runpod_dir: Path) -> None:
        name = recipe.metadata.name
        img = self.docker_image or f"<dockerhub-user>/{name}:latest"
        console.print(Panel(
            textwrap.dedent(f"""\
                [bold]Next steps to deploy on RunPod:[/bold]

                [cyan]1.[/cyan] Build and push the Docker image:
                   cd {runpod_dir}
                   docker build -t {img} .
                   docker push {img}

                [cyan]2.[/cyan] Register a serverless endpoint:
                   Go to [link]https://www.runpod.io/console/serverless[/link]
                   → New Endpoint → use image: {img}
                   → Select GPU: {self.gpu_type}

                [cyan]3.[/cyan] Copy the Endpoint ID, then run:
                   kdream run {name} --backend runpod \\
                       --endpoint-id <id> --prompt "..."
                   # or: export RUNPOD_ENDPOINT_ID=<id>
            """),
            title="kdream RunPod",
            expand=False,
        ))
