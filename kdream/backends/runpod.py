# Handler builder
# ---------------------------------------------------------------------------

class HandlerBuilder:
    """Generates handler.py — the RunPod serverless worker script."""

    def build(self, recipe: Recipe) -> str:
        """Return handler.py content string for *recipe*."""
        name = recipe.metadata.name
        script = recipe.entrypoint.script
        arg_mapping = recipe.entrypoint.args_mapping or {}
        has_models = bool(recipe.models)

        lines: list[str] = [
            "# Auto-generated by kdream — RunPod serverless handler",
            "from __future__ import annotations",
            "import os, subprocess, sys",
            "from pathlib import Path",
            "",
            "import runpod",
            "",
            f'WORK_DIR = "/workspace/{name}"',
            'MODELS_DIR = Path(os.environ.get("MODELS_DIR", "/runpod-volume/models"))',
            "",
        ]

        # _ensure_models function
        if has_models:
            lines += self._ensure_models_lines(recipe)
        else:
            lines += [
                "def _ensure_models():",
                "    pass  # No models defined in this recipe",
                "",
            ]

        # handler function
        lines += [
            "",
            "def handler(job):",
            '    """RunPod serverless handler."""',
            '    inputs = job.get("input", {})',
            "",
        ]

        # Apply defaults
        for inp_name, spec in recipe.inputs.items():
            if spec.default is not None:
                lines.append(f"    {inp_name} = inputs.get({inp_name!r}, {spec.default!r})")
            else:
                lines.append(f"    {inp_name} = inputs.get({inp_name!r})")

        if recipe.inputs:
            lines.append("")

        # Validate required inputs
        required_inputs = [n for n, s in recipe.inputs.items() if s.required]
        if required_inputs:
            for inp_name in required_inputs:
                lines += [
                    f"    if {inp_name} is None:",
                    f'        return {{"error": "Required input \'{inp_name}\' is missing"}}',
                ]
            lines.append("")

        lines += [
            "    _ensure_models()",
            "",
            f"    _cmd = [sys.executable, f'{{WORK_DIR}}/{script}']",
        ]

        # Build CLI args from inputs
        for inp_name, spec in recipe.inputs.items():
            flag = arg_mapping.get(inp_name, inp_name.replace("_", "-"))
            if spec.type == "boolean":
                lines += [
                    f"    if {inp_name}:",
                    f"        _cmd += ['--{flag}']",
                ]
            else:
                lines += [
                    f"    if {inp_name} is not None:",
                    f"        _cmd += ['--{flag}', str({inp_name})]",
                ]

        lines += [
            "",
            "    result = subprocess.run(",
            "        _cmd, capture_output=True, text=True, cwd=WORK_DIR",
            "    )",
            "    if result.returncode != 0:",
            '        return {"error": result.stderr[-2000:]}',
            "",
            "    # Collect file outputs",
            "    outputs: dict = {}",
        ]

        # Collect outputs
        if recipe.outputs:
            lines += [
                "    import glob as _glob",
                "    from pathlib import Path as _Path",
            ]
            for out in recipe.outputs:
                path_pat = out.path or f"outputs/*.{out.type}"
                lines += [
                    f"    _files_{out.name} = sorted(_glob.glob(f'{{WORK_DIR}}/{path_pat}'))",
                    f"    if _files_{out.name}:",
                    f"        outputs[{out.name!r}] = _files_{out.name}[-1]",
                ]
        else:
            lines.append("    # No outputs defined in recipe")

        lines += [
            "    return {",
            '        "output": result.stdout,',
            '        "outputs": outputs,',
            "    }",
            "",
            "",
            'runpod.serverless.start({"handler": handler})',
        ]

        return "\n".join(lines) + "\n"

    def _ensure_models_lines(self, recipe: Recipe) -> list[str]:
        lines: list[str] = [
            "def _ensure_models():",
            "    MODELS_DIR.mkdir(parents=True, exist_ok=True)",
        ]

        for model in recipe.models:
            dest_expr = f"MODELS_DIR / {model.destination!r}"
            if model.source == "huggingface":
                lines += [
                    f"    # {model.name}",
                    "    try:",
                    "        from huggingface_hub import snapshot_download",
                    f"        _dest = {dest_expr}",
                    f"        if not _dest.exists():",
                    f"            snapshot_download(",
                    f"                repo_id={model.id!r},",
                    "                local_dir=_dest,",
                    "                ignore_patterns=['*.msgpack', '*.h5', 'flax_model*'],",
                    "            )",
                    "    except Exception as _e:",
                    f"        print(f'Warning: failed to download {model.id}: {{_e}}')",
                ]
            elif model.source == "url":
                lines += [
                    f"    # {model.name} (URL download)",
                    "    import urllib.request",
                    f"    _dest = {dest_expr} / Path({model.id!r}).name",
                    "    _dest.parent.mkdir(parents=True, exist_ok=True)",
                    "    if not _dest.exists():",
                    f"        urllib.request.urlretrieve({model.id!r}, _dest)",
                ]
            elif model.source == "civitai":
                lines += [
                    f"    # {model.name} (CivitAI)",
                    "    import urllib.request",
                    "    _token = os.environ.get('CIVITAI_TOKEN', '')",
                    "    _headers = {'Authorization': f'Bearer {_token}'} if _token else {}",
                    f"    _civitai_url = f'https://civitai.com/api/download/models/{model.id}'",
                    f"    _dest = {dest_expr} / '{model.name}.safetensors'",
                    "    _dest.parent.mkdir(parents=True, exist_ok=True)",
                    "    if not _dest.exists():",
                    "        _req = urllib.request.Request(_civitai_url, headers=_headers)",
                    "        with urllib.request.urlopen(_req) as _r, open(_dest, 'wb') as _f:",
                    "            _f.write(_r.read())",
                ]

        lines.append("")
        return lines


