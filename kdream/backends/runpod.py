class DockerfileBuilder:
    """Generates a Dockerfile for a RunPod serverless endpoint."""

    BASE_IMAGE = "runpod/pytorch:2.4.0-py3.11-cuda12.4.1-devel-ubuntu22.04"

    def build(self, recipe: Recipe, skip_package_install: bool = False) -> str:
        """Return Dockerfile content string for *recipe*."""
        name = recipe.metadata.name
        repo = recipe.source.repo
        ref = recipe.source.ref or "main"
        extras = recipe.source.install_extras
        extras_joined = ",".join(extras)
        extra_suffix = f"[{extras_joined}]" if extras_joined else ""

        lines: list[str] = [
            f"FROM {self.BASE_IMAGE}",
            "",
            "# Environment",
            "ENV HF_HOME=/runpod-volume/.cache/huggingface \\",
            "    PYTHONUNBUFFERED=1 \\",
            "    MODELS_DIR=/runpod-volume/models \\",
            "    DEBIAN_FRONTEND=noninteractive",
            "",
            "# System deps",
            "RUN apt-get update -qq && apt-get install -y -q git curl && rm -rf /var/lib/apt/lists/*",
            "",
            "# Install uv",
            "RUN pip install --quiet uv",
            "",
            f"# Clone recipe source",
            f"RUN git clone --depth 1 --branch {ref} {repo} /workspace/{name}",
            "",
        ]

        if not skip_package_install:
            lines += [
                "# Install Python dependencies",
                f"WORKDIR /workspace/{name}",
                "RUN if [ -f requirements.txt ]; then uv pip install --system -r requirements.txt; \\",
                "    elif [ -f requirements/base.txt ]; then uv pip install --system -r requirements/base.txt; \\",
                "    elif [ -f requirements/main.txt ]; then uv pip install --system -r requirements/main.txt; fi",
                f"RUN if [ -f pyproject.toml ] || [ -f setup.py ]; then uv pip install --system -e '.{extra_suffix}'; fi",
                "",
            ]
        else:
            lines += [
                f"WORKDIR /workspace/{name}",
                "",
            ]

        lines += [
            "# Install runpod SDK",
            "RUN pip install --quiet runpod",
            "",
            "# Copy serverless handler",
            "COPY handler.py /workspace/handler.py",
            "",
            f"WORKDIR /workspace/{name}",
            "",
            'CMD ["python", "/workspace/handler.py"]',
        ]

        return "\n".join(lines) + "\n"


